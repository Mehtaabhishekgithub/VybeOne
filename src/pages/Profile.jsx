import React, { useEffect } from "react";
import axios from "axios";
import { serverUrl } from "../App";
import { useNavigate, useParams } from "react-router-dom";
import { useDispatch, useSelector } from "react-redux";
import { setProfileData, setUserData } from "../redux/userSlice";
import { IoArrowBackSharp } from "react-icons/io5";
import dp2 from "../assets/dp2.webp";
import Nav from "../components/Nav";

const Profile = () => {
  const { userName } = useParams();
  const dispatch = useDispatch();
  const { profileData, userData } = useSelector((state) => state.user);
  const navigate = useNavigate();

  const handleprofile = async () => {
    try {
      const result = await axios.get(
        `${serverUrl}/api/user/getProfile/${userName}`,
        { withCredentials: true },
      );

      dispatch(setProfileData(result.data));
    } catch (error) {
      console.log(error);
    }
  };

  const handleLogout = async () => {
    try {
      const result = await axios.get(`${serverUrl}/api/auth/signout`, {
        withCredentials: true,
      });
      dispatch(setUserData(null));
    } catch (error) {
      console.log(error);
    }
  };

  useEffect(() => {
    handleprofile();
  }, [userName, dispatch]);

  return (
    <div className="w-full min-h-screen bg-black">
      <div className="w-full h-[80px] flex justify-between items-center px-[30px] text-white">
        <div>
          <IoArrowBackSharp
            onClick={() => navigate("/")}
            className="w-[25px] h-[25px] cursor-pointer"
          />
        </div>
        <div className="font-semibold text-[20px]">{profileData?.userName}</div>
        <div
          onClick={handleLogout}
          className="font-semibold cursor-pointer text-[20px] text-blue-600"
        >
          LogOut
        </div>
      </div>

      <div
        className="w-full h-[150px] flex items-start gap-[20px] lg:gap-[50px]
    pt-[20px] px-[10px] justify-center"
      >
        <div className="w-[80px] h-[80px] md:w-[140px] md:h-[140px] border-2 border-black rounded-full cursor-pointer overflow-hidden">
          <img
            src={profileData?.profileImage || dp2}
            alt="Profile Picture"
            className="w-full object-cover"
          />
        </div>
        <div>
          <div className="font-semibold text-[22px] text-white">
            {profileData?.name}
          </div>
          <div className="font-semibold text-[17px] text-white">
            {profileData?.profession || "New User"}
          </div>
          <div className="font-semibold text-[17px] text-white">
            {profileData?.bio || "Engineer"}
          </div>
        </div>
      </div>

      <div
        className="w-full h-[100px] flex items-center justify-center gap-[40px] 
    md:gap-[60px] px-[20%] pt-[30px] text-white"
      >
        {/* post */}
        <div>
          <div className="text-white text-[22px] md:text-[30px] font-semibold">
            {profileData?.posts.length}
          </div>
          <div className="text-gray-400 text-[18px] md:text-[22px] font-semibold">
            Posts
          </div>
        </div>

        {/* follower */}
        <div>
          <div className="flex items-center justify-center gap-[20px]">
            <div className="flex relative">
              <div className="w-[40px] h-[40px] border-2 border-black rounded-full cursor-pointer overflow-hidden">
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
              <div
                className="w-[40px] h-[40px] absolute border-2 border-black rounded-full cursor-pointer
             overflow-hidden left-[9px]"
              >
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
              <div
                className="w-[40px] h-[40px] border-2 border-black rounded-full
             cursor-pointer overflow-hidden absolute left-[18px]
            "
              >
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
            </div>
            <div className="text-white text-[22px] md:text-[30px] font-semibold">
              {profileData?.followers.length}
            </div>
          </div>
          <div className="text-gray-400 text-[18px] md:text-[22px] font-semibold">
            Followers
          </div>
        </div>

        {/* following */}
        <div>
          <div className="flex items-center justify-center gap-[20px]">
            <div className="flex relative">
              <div className="w-[40px] h-[40px] border-2 border-black rounded-full cursor-pointer overflow-hidden">
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
              <div
                className="w-[40px] h-[40px] absolute border-2 border-black rounded-full cursor-pointer
             overflow-hidden left-[9px]"
              >
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
              <div
                className="w-[40px] h-[40px] border-2 border-black rounded-full
             cursor-pointer overflow-hidden absolute left-[18px]
            "
              >
                <img
                  src={profileData?.profileImage || dp2}
                  alt="Profile Picture"
                  className="w-full object-cover"
                />
              </div>
            </div>
            <div className="text-white text-[22px] md:text-[30px] font-semibold">
              {profileData?.following.length}
            </div>
          </div>
          <div className="text-gray-400 text-[18px] md:text-[22px] font-semibold">
            Following
          </div>
        </div>
      </div>
      <div className="w-full h-[80px] flex justify-center items-center gap-[20px] mt-[10px]">
        {profileData?._id == userData?._id && (
          <button
            onClick={() => navigate("/editprofile")}
            className="px-[10px] min-w-[150px] py-[5px] h-[40px]
            bg-[white] cursor-pointer rounded-2xl font-semibold "
          >
            Edit Profile
          </button>
        )}
        {profileData?._id != userData?._id && (
          <>
            <button
              className="px-[10px] min-w-[150px] py-[5px] h-[40px]
            bg-[white] cursor-pointer rounded-2xl font-semibold "
            >
              Follow
            </button>
            <button
              className="px-[10px] min-w-[150px] py-[5px] h-[40px]
            bg-[white] cursor-pointer rounded-2xl font-semibold "
            >
              Message
            </button>
          </>
        )}
      </div>

      <div className="w-full min-h-[100vh] flex justify-center">
        <div
          className="w-full max-w-[900px] flex flex-col items-center 
    rounded-t-[30px] bg-white relative gap-[20px] pt-[30px]"
        >
          <Nav />
        </div>
      </div>
    </div>
  );
};

export default Profile;
